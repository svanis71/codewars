import unittest

from kyu5.esolang_infiniticker import interpreter


class MyTestCase(unittest.TestCase):
    def test_something(self):
        tape = '++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++*>+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++*>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++**>+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++*>++++++++++++++++++++++++++++++++*>+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++*<<*>>>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++*<<<<*>>>>>++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++*>+++++++++++++++++++++++++++++++++*&'
        self.assertEqual(interpreter(tape), 'Hello world!', 'Should outpur Hello, World')

    def test_chr255(self):
        tape = '-/*&'
        self.assertEqual(interpreter(tape), chr(255), 'Should output ascii 0xff')

    def test_empty_string(self):
        tape = r'-\*&'
        self.assertEqual(interpreter(tape), '', 'Should return an empty string')


if __name__ == '__main__':
    unittest.main()
