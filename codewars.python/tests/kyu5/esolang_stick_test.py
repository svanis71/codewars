import unittest

from kyu5.esolang_stick import interpreter


class MyTestCase(unittest.TestCase):
    def test_loop(self):
        out = interpreter('+++++[-]+++++++*')
        self.assertEqual(chr(7), out)

    def test_loop_2(self):
        out = interpreter('+[+]*')
        self.assertEqual(chr(0), out)

    def test_hello_world(self):
        code = '''
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        ++++++++++*!++++++++++++++++++++++++++++++++++++++++++++++++++
        +++++++++++++++++++++++++++++++++++++++++++++++++++*!+++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        +++++++++++++++++++++++++++++++++++++*!+++++++++++++++++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        +++++++++++++++++++++++*!+++++++++++++++++++++++++++++++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        ++++++++++++*!++++++++++++++++++++++++++++++++++++++++++++*!++
        ++++++++++++++++++++++++++++++*!++++++++++++++++++++++++++++++
        +++++++++++++++++++++++++++++++++++++++++++++++++++++++++*!+++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++*!++++++++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        ++++++++++++++++++++++++++++++++++++++*!++++++++++++++++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        ++++++++++++++++++++++++*!++++++++++++++++++++++++++++++++++++
        ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
        ++*!+++++++++++++++++++++++++++++++++*!
        '''
        self.assertEqual(interpreter(code), 'Hello, World!')


if __name__ == '__main__':
    unittest.main()
